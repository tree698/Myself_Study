first = input()
second = input()

if first == '1/2' and second == '-1/3':
    print('Strange Quark')
elif first == '1/2' and second == '2/3':
    print('Charm Quark')
elif first == '1/2' and second == '-1':
    print('Electron Lepton')
elif first == '1/2' and second == '0':
    print('Neutrino Lepton')
elif first == '1' and second == '0':
    print('Photon Boson')
